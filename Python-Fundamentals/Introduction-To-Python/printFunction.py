#print function
#print directly anumber of string
print("Hello, World!")
print(123)

#print variable
name= "Neeraj"
age = 30

print(name)
print(age)

#print multiple varible
print("Name: ", name, "Age: ", age)

#custom separator
print("apple", "banana", "Mango", "Grapes", "cherry", sep=",")

#custom end character
print("Hello",end=" ")
print("World!")

#formated strings or f-string
name = "Neeraj Pandey"
age = 30
print(f"My name is {name} and I am {age} year old")

#escape characters
print("my name is Neeraj Pandey\nI am 30 year old.")
print("Name:\tNeeraj Pandey")
